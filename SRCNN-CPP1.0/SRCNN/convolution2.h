void Convolution11(IplImage* src[], IplImage* dst, float kernel[CONV1_FILTERS], float bias)
{
	/*遍历dst所有像素*/
	for (int row = 0; row < dst->height; row++)
	{
		for (int col = 0; col < dst->width; col++)
		{
			//对dst每个像素操作
			double temp = 0;	//存储dst[row][col]像素值16
			for (int i = 0; i < CONV1_FILTERS; i++)
			{
				unsigned int temppixel = 0;	//取出src[i].[row][col]像素值
				temppixel += (unsigned char)(src[i]->imageData + row*src[i]->widthStep)[2 * col];
				temppixel <<= 8;
				temppixel += (unsigned char)(src[i]->imageData + row*src[i]->widthStep)[2 * col + 1];

				temp += temppixel * kernel[i];
			}
			temp += bias;

			unsigned int tempINT = 0;
			if (temp > 0)
			{
				tempINT = (unsigned int)(temp + 0.5);
			}

			(dst->imageData + row*dst->widthStep)[2 * col + 1] = (unsigned char)tempINT;
			(dst->imageData + row*dst->widthStep)[2 * col] = (unsigned char)(tempINT >> 8);
			//对dst每个像素操作
		}
	}
}

